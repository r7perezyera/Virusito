package mx.itesm.equipo5;

import com.badlogic.gdx.Gdx;
import com.badlogic.gdx.graphics.g2d.BitmapFont;
import com.badlogic.gdx.graphics.g2d.GlyphLayout;
import com.badlogic.gdx.graphics.g2d.SpriteBatch;

public class Texto {

    private BitmapFont font;

    public Texto() {
        // load font
        font = new BitmapFont(Gdx.files.internal("Fuentes/fuenteCuadrada.fnt"));  // font name
    }

    public void mostrarTexto(SpriteBatch batch, String mensaje, float x, float y) {
        GlyphLayout glyph = new GlyphLayout();
        glyph.setText(font, mensaje);
        font.setColor(1,1,1,1);
        float anchoTexto = glyph.width;
        font.draw(batch, glyph, x - anchoTexto/2, y); // centrado en x respecto al texto, NO la screen

    }

    // text will be shown (drawn) within the batch.begin and .end lines,
    // in the class containing those
    
}
